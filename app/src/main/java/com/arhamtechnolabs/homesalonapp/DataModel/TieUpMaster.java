package com.arhamtechnolabs.homesalonapp.DataModel;

public class TieUpMaster {

    String type, name, contact_no, company_name, time_for_callback, comments;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContact_no() {
        return contact_no;
    }

    public void setContact_no(String contact_no) {
        this.contact_no = contact_no;
    }

    public String getCompany_name() {
        return company_name;
    }

    public void setCompany_name(String company_name) {
        this.company_name = company_name;
    }

    public String getTime_for_callback() {
        return time_for_callback;
    }

    public void setTime_for_callback(String time_for_callback) {
        this.time_for_callback = time_for_callback;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }
}
